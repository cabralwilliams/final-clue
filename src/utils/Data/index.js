export function searchMatches(regex, choices) {
    return choices.filter(choice => choice.match(regex));
};

export function createAnswerSet(answerArray) {
    const newSet = new Set(answerArray);
    return [...newSet];
}

export class FinalJeopardy {
    constructor(clue, category, correctAnswer, responseList, matchCount = true, isPerson = false, gameDate = new Date(), wasRunaway = false, notes = "") {
        this.clue = clue;
        this.category = category;
        //This will be an array of values
        this.correctAnswer = correctAnswer;
        this.responseList = responseList;
        this.matchCount = matchCount;
        this.isPerson = isPerson;
        this.gameDate = gameDate;
        this.wasRunaway = wasRunaway;
        this.notes = notes;
        this.totalAnswers = this.correctAnswer.length;
    }

    addToResponses(newResponseList) {
        this.responseList = [...this.responseList, ...newResponseList];
    }
}

